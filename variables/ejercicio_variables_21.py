# Ejercicio 21: Pedir dos números al usuario
# y mostrar la suma y el producto de ambos

# Pedimos al usuario el primer número
numero1 = int(input("Escribe el primer número: "))

# Pedimos al usuario el segundo número
numero2 = int(input("Escribe el segundo número: "))

# Calculamos la suma de los dos números
suma = numero1 + numero2

# Calculamos el producto de los dos números
producto = numero1 * numero2

# Mostramos los resultados en pantalla
print(f"La suma de los números es: {suma}")
print(f"El producto de los números es: {producto}")
