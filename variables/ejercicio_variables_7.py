# Ejercicio 7: Calcular el número de minutos en un año

# Definimos los días del año
dias_anio = 365

# Definimos las horas por día y minutos por hora
horas_dia = 24
minutos_hora = 60

# Calculamos los minutos en un día
minutos_dia = horas_dia * minutos_hora

# Calculamos los minutos en un año
minutos_anio = dias_anio * minutos_dia

# Mostramos el resultado
print(minutos_anio)