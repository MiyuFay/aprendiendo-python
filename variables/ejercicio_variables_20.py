# Ejercicio 20: Pedir tres números al usuario
# y mostrar el producto de los tres números

# Pedimos al usuario los tres números (convertidos a entero)
numero1 = int(input("Escribe el primer número: "))
numero2 = int(input("Escribe el segundo número: "))
numero3 = int(input("Escribe el tercer número: "))

# Calculamos el producto de los tres números
producto = numero1 * numero2 * numero3

# Mostramos el resultado en pantalla
print(f"El producto es: {producto}")
