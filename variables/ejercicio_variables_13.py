# Ejercicio 13: Historia personalizada con nombre y año

# Pedimos al usuario el nombre del protagonista y el año
nombre = input("Escribe un nombre: ")
anio = input("Escribe un año: ")

# Mostramos la historia usando los datos ingresados
print(f"{nombre} era un valiente caballero, nacido en el año {anio}.")
print(f"Una mañana, {nombre} escuchó un gran alboroto: un dragón se acercaba al pueblo.")
print(f"Solo {nombre} podía salvar a los habitantes del peligro que se avecinaba.")