# Ejercicio 2: Comprobar un año de publicación y mostrar el autor de la novela

# Este ejercicio se ha adaptado de ejercicios disponibles en la web:
# https://programming-23.mooc.fi/part-1/5-conditional-statements

# Pedimos al usuario que escriba un número entero
anio = int(input("Escribe un año: "))

# Si el año coincide con la publicación de la novela, mostramos el autor
if anio == 1813:
    print("Jane Austen")

